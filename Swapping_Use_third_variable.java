
public class Swapping_Use_third_variable {
	public static void main(String[] args) {
		int a = 12;
		int b = 13;
		int c;
		System.out.println("Before Swapping");
		System.out.println(a + " " + b);
		c = a;
		a = b;
		b = c;
		System.out.println("After Swapping");
		System.out.println(a + " " + b);
		
	}

}
